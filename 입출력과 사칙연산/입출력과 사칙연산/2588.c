/* 2588번
(세 자리 수) × (세 자리 수)는 다음과 같은 과정을 통하여 이루어진다.

(사진)

(1)과 (2)위치에 들어갈 세 자리 자연수가 주어질 때 (3), (4), (5), (6)위치에 들어갈 값을 구하는 프로그램을 작성하시오.*/

#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
int main(void)
{
	int A, B, B1, result;
	scanf("%d", &A);
	scanf("%d", &B);

	B1 = B;

	while (B > 0)
	{
		result = B % 10;
		printf("%d\n", A * result);
		B /= 10;
	}

	printf("%d\n", A * B1);

	return 0;
}